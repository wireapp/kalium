package com.wire.kalium.monkeys

class DefaultChunkSplit(private val chunkSize: Int, private val maxChunks: Int) : SplitStep {
    override suspend fun invoke(allUsers: List<UserData>): List<List<UserData>> {
        return allUsers.chunked(chunkSize).take(maxChunks)
    }
}
